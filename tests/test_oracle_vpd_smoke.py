def test_allowed_scope_returns_only_permitted_rows(vpd_client: OracleVPDClient) -> None:
    rows = vpd_client.fetch_all(
        f"select scope_value from {OWNER_USER}.{TABLE_NAME} order by row_id",
        department_id="sales",
        principal_id=f"user-{uuid4()}",
    )

    assert rows == [("sales-only-row",)]


def test_database_vpd_cannot_be_bypassed_by_application_filter(vpd_client: OracleVPDClient) -> None:
    rows = vpd_client.fetch_all(
        f"select scope_value from {OWNER_USER}.{TABLE_NAME} where department_id = 'finance'",
        department_id="sales",
        principal_id=f"user-{uuid4()}",
    )

    assert rows == []


def test_context_is_cleared_before_connection_reuse(vpd_client: OracleVPDClient) -> None:
    sales_rows = vpd_client.fetch_all(
        f"select scope_value from {OWNER_USER}.{TABLE_NAME} order by row_id",
        department_id="sales",
        principal_id=f"user-{uuid4()}",
    )
    cleared_value = vpd_client.fetch_scalar(f"select sys_context('{CONTEXT_NAMESPACE}', 'DEPARTMENT_ID') from dual")
    finance_rows = vpd_client.fetch_all(
        f"select scope_value from {OWNER_USER}.{TABLE_NAME} order by row_id",
        department_id="finance",
        principal_id=f"user-{uuid4()}",
    )

    assert sales_rows == [("sales-only-row",)]
    assert cleared_value is None
    assert finance_rows == [("finance-only-row",)]
