CREATE TABLE AIAL_REGION_DIM (
  REGION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  REGION_CODE VARCHAR2(32) NOT NULL,
  REGION_NAME VARCHAR2(100) NOT NULL,
  DEPARTMENT_SCOPE VARCHAR2(32) NOT NULL
);

CREATE TABLE AIAL_CHANNEL_DIM (
  CHANNEL_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  CHANNEL_CODE VARCHAR2(32) NOT NULL,
  CHANNEL_NAME VARCHAR2(100) NOT NULL
);

CREATE TABLE AIAL_PRODUCT_DIM (
  PRODUCT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PRODUCT_CODE VARCHAR2(32) NOT NULL,
  PRODUCT_NAME VARCHAR2(100) NOT NULL,
  CATEGORY_NAME VARCHAR2(100) NOT NULL
);

CREATE TABLE AIAL_SALES_FACT (
  SALES_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PERIOD_DATE DATE NOT NULL,
  DEPARTMENT_SCOPE VARCHAR2(32) NOT NULL,
  REGION_ID NUMBER NOT NULL REFERENCES AIAL_REGION_DIM(REGION_ID),
  CHANNEL_ID NUMBER NOT NULL REFERENCES AIAL_CHANNEL_DIM(CHANNEL_ID),
  PRODUCT_ID NUMBER NOT NULL REFERENCES AIAL_PRODUCT_DIM(PRODUCT_ID),
  GROSS_REVENUE NUMBER(18,2) NOT NULL,
  RETURNS_AMOUNT NUMBER(18,2) NOT NULL,
  NET_REVENUE NUMBER(18,2) NOT NULL,
  COST_AMOUNT NUMBER(18,2) NOT NULL,
  BUDGET_AMOUNT NUMBER(18,2) NOT NULL,
  ORDER_COUNT NUMBER NOT NULL,
  STATUS VARCHAR2(20) DEFAULT 'POSTED' NOT NULL
);

CREATE TABLE AIAL_BUDGET_FACT (
  BUDGET_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PERIOD_DATE DATE NOT NULL,
  DEPARTMENT_SCOPE VARCHAR2(32) NOT NULL,
  REGION_ID NUMBER NOT NULL REFERENCES AIAL_REGION_DIM(REGION_ID),
  CHANNEL_ID NUMBER NOT NULL REFERENCES AIAL_CHANNEL_DIM(CHANNEL_ID),
  BUDGET_REVENUE NUMBER(18,2) NOT NULL,
  BUDGET_COST NUMBER(18,2) NOT NULL
);

CREATE TABLE AIAL_DOCUMENT_CATALOG (
  DOCUMENT_ID VARCHAR2(64) PRIMARY KEY,
  TITLE VARCHAR2(255) NOT NULL,
  DEPARTMENT_SCOPE VARCHAR2(32) NOT NULL,
  CLASSIFICATION NUMBER NOT NULL,
  SOURCE_TRUST VARCHAR2(32) NOT NULL,
  EFFECTIVE_DATE DATE NOT NULL
);

