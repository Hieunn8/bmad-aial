CREATE OR REPLACE VIEW AIAL_SALES_DAILY_V AS
SELECT
  s.PERIOD_DATE,
  s.DEPARTMENT_SCOPE,
  r.REGION_CODE,
  r.REGION_NAME,
  c.CHANNEL_CODE,
  c.CHANNEL_NAME,
  p.PRODUCT_CODE,
  p.PRODUCT_NAME,
  p.CATEGORY_NAME,
  s.GROSS_REVENUE,
  s.RETURNS_AMOUNT,
  s.NET_REVENUE,
  s.COST_AMOUNT,
  s.BUDGET_AMOUNT,
  s.ORDER_COUNT,
  (s.NET_REVENUE - s.COST_AMOUNT) AS GROSS_MARGIN
FROM AIAL_SALES_FACT s
JOIN AIAL_REGION_DIM r ON r.REGION_ID = s.REGION_ID
JOIN AIAL_CHANNEL_DIM c ON c.CHANNEL_ID = s.CHANNEL_ID
JOIN AIAL_PRODUCT_DIM p ON p.PRODUCT_ID = s.PRODUCT_ID
WHERE s.STATUS = 'POSTED';

